    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter all the fields", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if(!jTextField2.getText().matches("[0-9]+")){
                JOptionPane.showMessageDialog(this, "Please enter a valid safety score", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                Driver driver = new Driver(jTextField1.getText(), Integer.parseInt(jTextField2.getText()));
                Configuration.getShippingNetwork().addDriver(driver);
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(this, "Driver added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

