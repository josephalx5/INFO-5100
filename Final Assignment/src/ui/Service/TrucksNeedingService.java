    private void populateTables() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Truck truck : Configuration.getManufacturer().getService().getProspectiveTrucks()){
            model.addRow(new Object[]{truck.getTruckId(), truck.getTruckCapacity(), truck.getFuelType(), truck.getMileage(), truck.getTruckModel()});
        }
    }

