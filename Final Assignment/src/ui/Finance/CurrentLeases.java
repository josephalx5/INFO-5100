    private void populateTable() {
        id.setEnabled(false);
        update.setEnabled(false);
        leaseTerm.setEnabled(false);
        intrestRate.setEnabled(false);
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for (Lease lease : Configuration.getPurchase().getLeases()) {
            model.addRow(new Object[]{lease.getLeaseId(), lease.getLeaseTerm(), lease.getLeaseAmount(), lease.getInterestRate(), lease.getOrganization().getName()});
        }
    }

