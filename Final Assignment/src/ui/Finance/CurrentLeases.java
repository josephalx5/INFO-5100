    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String id = jTable2.getValueAt(row, 0).toString();
            Lease lease = Configuration.getPurchase().getLeases().stream().filter(l -> l.getLeaseId().equals(id)).findFirst().get();
            this.id.setText(lease.getLeaseId());
            leaseTerm.setText(String.valueOf(lease.getLeaseTerm()));
            intrestRate.setText(String.valueOf(lease.getInterestRate()));
            leaseTerm.setEnabled(true);
            intrestRate.setEnabled(true);
            update.setEnabled(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        if (leaseTerm.getText().isEmpty() || intrestRate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all the fields", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String leaseId = id.getText();
            Lease lease = Configuration.getPurchase().getLeases().stream().filter(l -> l.getLeaseId().equals(leaseId)).findFirst().get();
            lease.setLeaseTerm(Integer.parseInt(leaseTerm.getText()));
            lease.setInterestRate(Double.parseDouble(intrestRate.getText()));
            JOptionPane.showMessageDialog(this, "Lease updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            id.setText("");
            leaseTerm.setText("");
            intrestRate.setText("");
            leaseTerm.setEnabled(false);
            intrestRate.setEnabled(false);
            populateTable();
        }
    }//GEN-LAST:event_updateActionPerformed


